    private void jButton_Them1NoiDungLuaChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Them1NoiDungLuaChonActionPerformed
        // TODO add your handling code here:
        String res = jTextArea_NoiDungLuaChon.getText().toString().trim();
        
        if(res.isEmpty() || res.equals("Nhập nội dung lựa chọn vào đây..."))
        {
            JOptionPane.showMessageDialog(null,"Nội dung lựa chọn không được trống!!!", "Cảnh báo",WARNING_MESSAGE);
            jTextArea_NoiDungLuaChon.requestFocus();
            return;
        }
        else
        {
            char c = 'A';
            c+=dem++;
            cauHoi.danhsachLuaChon.put(c, new LuaChon(c, res));
            System.out.println("---> Lua chọn mơi them: " + c +"  : "+ cauHoi.danhsachLuaChon.get(c));
            LoadDanhSachLuaChon();
            jTextArea_NoiDungLuaChon.setText("Nhập nội dung lựa chọn vào đây...");
        }
        
    }//GEN-LAST:event_jButton_Them1NoiDungLuaChonActionPerformed

