    private void jButtonLuu_CSDLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLuu_CSDLActionPerformed
        // TODO add your handling code here:
        if(cauHoi.getNoiDung().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Nội dung CÂU HỎI không được trống!!!", "Cảnh báo",WARNING_MESSAGE);
            jTextArea_NoiDungCauHoi.requestFocus();
            return;
        }
        if(cauHoi.danhsachLuaChon.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Danh sách LỰA CHỌN không được trống!!!", "Cảnh báo",WARNING_MESSAGE);
            jTextArea_NoiDungLuaChon.requestFocus();
            return;
        }
        if(cauHoi.dapAn == null)
        {
            JOptionPane.showMessageDialog(null,"BẠN CHƯA CHỌN ĐÁP ÁN!!!", "Cảnh báo",WARNING_MESSAGE);
            return;
        }
        if(Modify.ThemCauHoiVaoCSDL(cauHoi))
        {
            JOptionPane.showMessageDialog(null,"Thêm Câu hỏi vào CSDL thành công!", "Thông báo",INFORMATION_MESSAGE);
            this.setVisible(false);
            MainFrame.show.LoadCauHoiRaBang();
            return;
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Thêm Câu hỏi vào CSDL Thất bại!", "Thông báo",WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButtonLuu_CSDLActionPerformed

    private void jButtonXoaLCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonXoaLCActionPerformed
        // TODO add your handling code here:
        if(jTable_DsLuaChon.getSelectedRow() == -1)
        {
            JOptionPane.showMessageDialog(null,"bạn chưa chọn Lựa Chọn để xóa!!!", "Thông báo",WARNING_MESSAGE);
            return;
        }
        
        if(cauHoi.getDapAn().getTenLC()!=null && cauHoi.dapAn.getTenLC().equals(cauHoi.danhsachLuaChon.get(jTable_DsLuaChon.getValueAt(jTable_DsLuaChon.getSelectedRow(),1).toString().charAt(0))))
        {
            JOptionPane.showMessageDialog(null,"BẠN KHÔNG ĐƯỢC LOẠI BỎ ĐÁP ÁN!!!", "Cảnh báo",WARNING_MESSAGE);
            return;
        }
        if(cauHoi.danhsachLuaChon.size()>0)
        {
            cauHoi.danhsachLuaChon.remove(jTable_DsLuaChon.getValueAt(jTable_DsLuaChon.getSelectedRow(),0).toString().charAt(0));
            LoadDanhSachLuaChon();
        }
        if(cauHoi.danhsachLuaChon.isEmpty())
        {
            jButtonDatLamDapAn.setEnabled(false);
            cauHoi.thayDapAn(null);
            jLabel_DapAN.setText("Chưa có");
            return;
        }
        else{
            if(!cauHoi.getNoiDung().isEmpty())
                jButtonLuu_CSDL.setEnabled(true);   
            
        }
    }//GEN-LAST:event_jButtonXoaLCActionPerformed

